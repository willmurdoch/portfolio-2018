                      <div id="play-of-the-game" class="about-section">
                        <h2>PLAY OF THE GAME</h2>
                        <p id="minor-penalties"><strong>Minor Penalties:</strong> On two minute personal fouls, the penalized player is released from the penalty box if a goal is scored before the expiration of the two minutes.</p>
                        <p id="major-penalties"><strong>Major Penalties:</strong> On five minute major personal fouls, the penalized player stays in the box for the duration of the penalty, though the offending team returns to full strength if two goals are scored against them during the five minutes. When a second major penalty is imposed on the same player in a game, an automatic game misconduct penalty shall be imposed.</p>
                        <p id="use-of-penalty-shot"><strong>Use of Penalty Shot:</strong> Since a team cannot be more than two men down at a time, if a third penalty is called, the official will award a penalty shot to the non-offending team.</p>
                        <p id="ejection-from-game"><strong>Ejection from Game:</strong> Players can be ejected from a game for several reasons including being the third man participating in a fight or accumulating two major penalties in one game.</p>
                        <p id="slow-whistle-delayed-penalty"><strong>Slow Whistle (Delayed Penalty):</strong> If a defending player commits a minor or major penalty against an opponent in possession of the ball, the 30-second shot clock expires, or a goal is scored or possession is gained by the non-offending team.</p>
                        <p id="coincidental-penalties"><strong>Coincidental Penalties:</strong> When each team is given the same amount of penalty time arising out of the same incident, the offending players shall not be released until the expiration of the penalty. Teams do not lose floor strength, and the ball is awarded to the team who was in possession prior to the fouls.</p>
                        <p id="field-and-goals"><strong>Field and Goals:</strong> Indoor lacrosse is played on a hockey rink covered by an artificial turf playing surface, which is usually referred to as the floor or the carpet (as opposed to the field). There must be boards around the sides of a minimum height of 3' high. Dimensions are 200' x 85' but may be altered.</p>
                        <p id="goals"><strong>Goals:</strong> are 4' (high) x 4'9" (wide). The circle around the goal known as the crease is 9'3" in diameter. An offensive player is not allowed to step into the crease area.</p>
