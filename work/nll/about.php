                      <div id="overview" class="about-section">
                        <h2>OVERVIEW</h2>
                        <p>Indoor lacrosse is played inside the confines of an ice hockey rink, with glass and rink boards intact. The playing surface consists of a green dieter turf carpet that is laid down over the hockey ice. The two teams combine to score a total of 25 goals on average during an NLL game.</p>
                        <p>Each team has five runners (forwards, transition players and defensemen) and a goaltender on the floor during the game. Each team dresses 18 players (16 runners and two goaltenders) per game, and the players rotate on and off the floor in shifts, similar to ice hockey. The game consists of four quarters, each fifteen minutes in length. A game that is tied at the end of regulation is decided in a sudden-death overtime. There are no tie games in professional indoor lacrosse.</p>
                      </div>
                      <div id="nll-rules" class="about-section">
                        <h2>NLL RULES</h2>
                        <p id="rosters"><strong>Rosters:</strong> 20-man roster, each team dresses 18 players for games (16 runners and two goalies). A team shall be composed of six (6) players on the floor, 5 runners and one goalie.</p>
                        <p id="time-format"><strong>Time Format:</strong> Four 15-minute quarters; two minutes between quarters; 12 minute halftime.</p>
                        <p id="timeouts"><strong>Timeouts:</strong> Each team may take one 45-second timeout per half. A TV game has two timeouts per quarter.</p>
                        <p id="sudden-death-overtime"><strong>Sudden Death Overtime:</strong> Games ending regulation play with a tie score are decided by a sudden death overtime period. Play continues until a goal is scored. More than one overtime period is played if necessary.</p>
                        <p id="8-second-violation"><strong>8-Second Violation:</strong> Occurs when team on offense fails to advance the ball past midfield within 8 seconds after taking possession at their end.</p>
                        <p id="face-offs"><strong>Face-Offs:</strong> To determine possessions at the start of each quarter and after every goal, two players face their sticks at midfield with a referee placing the ball between the heads of the sticks.</p>
                        <p id="shot-clock"><strong>Shot Clock:</strong> A 30-second clock begins (counting down) when a team assumes possession of the ball. The offensive team must put a shot on goal during that time or they will lose possession. If they do shoot on goal (without scoring) and recover possession of the ball (via rebound/loose ball recovery), the clock is reset for a new 30 seconds.</p>
