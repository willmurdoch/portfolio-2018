                        <p id="power-play"><strong>Power Play:</strong> When a team has an extra man advantage because the other team has at least one player in the penalty box</p>
                        <p id="screen-shot"><strong>Screen Shot:</strong> When the goaltender can't see a shot because someone is in the way.</p>
                        <p id="shorthanded"><strong>Shorthanded:</strong> When a team has one or more players in the penalty box and the opponent is at full-strength, or has more players on the floor.</p>
                        <p id="loss-of-possesion"><strong>Loss of Possession:</strong> Illegal screens, 30 second shot clock violation, 8 second half court violation, loose ball push, and illegal procedure during faceoffs are among the acts that can cause a team to lose possession of the ball.</p>
