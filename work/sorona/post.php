					<p><strong>AMSTERDAM – DuPont Industrial Biosciences (DuPont) received the 2016 Bio Business Award for Breakthrough Bio-Based Technology Platform at the World Bio Markets Conference awards ceremony in Amsterdam. A global leader in the Bioeconomy, DuPont was recognized for DuPont™ Sorona® biopolymer, a sustainably sourced, high-performance fiber that contains 37 percent renewable plant-based ingredients, and uses 30 percent less energy and releases 63 percent fewer greenhouse gas emissions compared to commonly used nylon 6.</strong></p>
					<p>Sorona® carpet fiber has revolutionized the textiles industry by setting new standards for soft, stain-resistant carpets. In apparel, DuPont demonstrated that sustainable material does not require a sacrifice in performance through a 2014 partnership with Vipul Sarees – a leader in the Indian saree market. Vipul uses Sorona® fiber to make soft and colorful sarees in chiffon, georgette, satin and crepe materials that rival the luxurious feel of silk. This partnership provides an alternative for customers who want to use their purchasing power to enjoy superior feel while reducing their use of fossil fuels and greenhouse gas footprint.</p>
					<blockquote class="align-right">
						<p>Sorona® allows designers the freedom to be creative without compromising on performance or quality.</p>
						<span class="attribution">Simon Herriott</span>
					</blockquote>
					<p>“As a science company with a long history in the textiles industry, DuPont continues to innovate with technologies like Sorona® that allow designers the freedom to be creative without compromising on performance or quality,” said Simon Herriott, business director for Biomaterials at DuPont. “With Sorona®, everyone benefits. Textile mills, designers and manufacturers can offer consumers sustainable clothing that is supremely wearable, easy to care for and more gentle on the planet.”</p>
					<p class="align-left">
						<img src="img/blog/article1.jpg" alt="" />
					</p>
					<p>DuPont has unparalleled experience in the textiles market; the industry leader has invented nylon, Lycra®, Kevlar® and developed rayon (the world’s first man-made fiber) and other household materials over its 212-year history. But recently, the company has focused its scientists and engineers on a new challenge: creating everyday materials from renewable sources rather than from petroleum.</p>
					<p>With the creation of Sorona®, DuPont has made it possible for textile designers, mills and manufacturers to produce the sustainable products that consumers want with the performance benefits they demand. From renewably sourced monomer production, to lower dyeing temperatures to easy care for consumers, the sustainably sourced fiber provides sustainability benefits throughout the value chain.</p>
					<p>DuPont™ Sorona® received the 2015 Breakthrough Bio-Based Technology Platform award at the 10th annual World Bio Markets Conference. The conference brought together industrial, academic and policy experts to provide a comprehensive look at the opportunities in the bio-based economy, including end-user markets for renewable fuels, bio-based chemicals and bio-based products.</p>
					<p>DuPont (NYSE: DD) has been bringing world-class science and engineering to the global marketplace in the form of innovative products, materials, and services since 1802. The company believes that by collaborating with customers, governments, NGOs, and thought leaders we can help find solutions to such global challenges as providing enough healthy food for people everywhere, decreasing dependence on fossil fuels, and protecting life and the environment.</p>
